                    <br>
                    &nbsp; &nbsp; &nbsp;Model V adalah model pengembangan perangkat lunak yang didasarkan pada hubungan antara setiap fase pengembangan siklus hidup yang tercantum dalam model Watterfall yang merupakan pengembangan perangkat lunak dan fase yang terkait pengujian.</li>
					<br>
					<br>
					&nbsp; &nbsp; &nbsp;Tahapan pada V Model dibagi menjadi 2 garis besar yaitu tahap Verifikasi dan Validasi atau testing. Tahap Verfiikasi mengacu kepada usaha penyesuaian spesifikasi software dengan kebutuhan klien/konsumen, tahapan ini meliputi serangkaian kegiatan sebagai berikut:
                    <br>
                    <ul style="color:white">
					<li>Business Case, Merupakan tahapan awal yang menggambarkan kebutuhan/harapan konsumen terhadap sistem yang akan dikembangkan, termasuk manfaat sistem terhadap konsumen dan perkiraan biaya yang harus disediakan.</li>
					<br>
					<li>Requiremen, pada fase ini klien mendapatkan gambaran atau diminta memberikan gambaran kebutuhan yang diharapkan dapat dipenuhi oleh software, baik kebutuhan fungsional maupun non fungsional.</li>
					<br>
					<li>Analisis Informasi ,setelah diperoleh spesifikasi sistem dari fase requirement, selanjutnya aktivitas difokuskan bagaimana cara kerja software untuk memenuhi kebutuhan tersebut, termasuk metode, hardware dan software apa saja yang diperlukan untuk mencapai kebutuhan yang sudah didefinisikan.</li>
					<br>
					<li>Perancangan Sistem, pada tahapan ini akan dibuat rancangan software secara lebih terinci sesuai spesifikasi yang sudah disepakati.</li>
					<br>
					<li>Unit Design, merancang setiap elemen/unit software termasuk rancangan modul/program, antarmuka, database dan lain-lain.</li>
					<br>
					<li>Development, merealisasikan hasil rancangan menjadi satu aplikasi/program tertentu.</li>
                    <br>
					<br>
					Tahapan Validasi merupakan serangkaian tahapan yang mengacu kepada kesesuaian software dengan spesifikasi yang sudah ditetapkan. Tahapan ini dicapai melalui serangkaian pengujian/testing sebagai berikut:
					<br>
					<br>
					<li>Unit test menguji setiap komponen/unit program apakah sesuai dengan rancangan unit yang sudah ditetapkan. Secara teoritis seharusnya pengujian dilakukan oleh orang tertentu yang bertugas sebagai software tester, tetapi dalam kenyataannya seringkali unit testing dilakukan oleh programmer sendiri.</li>
					<br>
					&nbsp; &nbsp; &nbsp;Interface test setelah semua komponen diuji secara terpisah, tahapan selanjutnya dilakukan interface test untuk melihat sejauh mana setiap komponen dapat berinteraksi satu sama lain sesuai dengan fungsi yang diharapkan.
					<br><br>
					&nbsp; &nbsp; &nbsp; &nbsp;System test setelah semua interface berjalan dengan baik, selanutnya dilakukan system test untuk melihat sejauh mana sistem/software dapat memenuhi kebutuhan secara keseluruhan. System testing bersifat menyeluruh dan tidak dapat dilakukan berdasarkan fungsionalitas sistem yang diuji secara terpisah. Aktivitas pada system testing termasuk melakukan pengujian hal-hal berikut:
					<br>
					<li>Performance – apakah kinerja sistem sesuai dengan target yang sudah didefinisikan sebelumnya.</li>
					<br>
					<li>Volume – apakah software/sistem dapat menampung volume informasi yang cukup besar.</li>
					<br>
					<li>Stress – apakah software/sistem dapat menampung sejumlah informasi pada waktu-waktu tertentu.</li>
					<br>
					<li>Documentation – apakah semua dokumentasi penting sudah disiapkan.</li>
					<br>
					<li>Robustness – apakah software/sistem cenderung stabil pada berbagai kondisi diluar dugaan/ekstrim.</li>
					<br><br>
					<li>Acceptance test merupakan aktivitas untuk menguji sejauh mana sistem/software dapat membantu memecahkan business case, dalam artian apakah sistem/software tersebut sudah sesuai dengan harapan konsumen/klien dan sejauh mana manfaat sistem/software ini bagi klien. Test ini sering kali disebut sebagai User Acceptance Test (UAT).</li>
					<br>
					<li>Release testing test ini dilakukan untuk menguji sejauh mana sistem/software dapat mendukung aktivitas organisasi dan berjalan dengan harmonis sesuai dengan kegiatan rutin organisasi. Beberapa pertanyaan coba dijawab pada fase ini misalnya apakah software tersebut mempengaruhi sistem lain? Apakah software tersebut kompatibel dengan sistem lain? Bagaimana kinerja sistem sebenarnya di dalam organisasi?
				</li><br></ul>			
