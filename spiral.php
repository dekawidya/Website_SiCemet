					</li>
					<li>Mudah dalam mengestimasi biaya karena proses pembuatan prototype yang jelas dan terencana dalam tahapan yang sistematis<br>
					</li>
					<li>Manajemen dan analisa risiko yang lebih cepat dan mudah<br>
					</li>
					<li>Mudah dalam melakukan perubahan kebutuhan dan dokumentasi<br>
					</li>
					<li>Produksi software bisa terjadi lebih cepat<br>
					</li> </ul>
				   
