                <p class="text-white mb-4" >
                <ul style="color:white">
                    <br>
                    &nbsp; &nbsp; &nbsp; Model spiral diperkenalkan pertama kali oleh Barry Boehm pada makalahnya yang berjudul Spiral Model of Software Development and Enhancement. Barry Boehm menjelaskan bahawa model spiral merupakan model yang sangat berguna untuk melakukan pembangunan proyek-proyek besar dan prosesnya dilakukan dengan memperhatikan resiko proyek sehingga pada akhirnya akan menghasilkan model proses yang tepat sesuai kebutuhan pengguna.
					<br><br>
					&nbsp; &nbsp; &nbsp;Model Spiral adalah salah satu metode yang dapat digunakan dalam pengembangan perangkat lunak. Model spiral merupakan penggabungan dari model prototyping dan model waterfall. Model prototyping yang fokus pada penyajian atau presentasi kepada user dengan format input dan output kemudian perangkat lunak akan dievaluasi. Model waterfall yang fokus kepada proses pengembangan perangkat lunak yang sistematis atau berurutan. Model spiral menekankan pada Analisa resiko setiap tahapannya.
					<br><br>
					&nbsp; &nbsp; &nbsp;Fungsi model spiral adalah untuk melakukan perubahan, penambahan dan pengembangan perangkat lunak dengan memaksimalkan aspek kecepatan dan ketepatan berdasarkan keinginan dan kebutuhan penggunanya.
					<br><br>
					Tahapan dalam Spiral Model<br>
					Dalam penerapan Model Spiral, terdapat lima tahapan untuk merealisasikan penggunaannya, yaitu sebagai berikut:<br>
					<li>Tahap Liason<br>
					Tahap ini berhubungan dengan komunikasi antara pihak-pihak yang terlibat dalam pengembangan softaware (seperti: system analyst) dengan pelanggan (user). Tujuannya adalah memperbaiki dan mengembangan software sesuai kebutuhan dan keinginan hingga memuaskan pelanggan.
					</li>
					<br>
					<li>Tahap planning<br>
					Tahap perencanaan meliputi estimasi biaya yang digunakan, batas waktu, pengaturan jadwal, identifikasi lingkungan kerja, sumber-sumber informasi untuk melakukan iterasi (Teknik perulangan). Hasil dari tahapan ini adalah dokumen spesifikasi kebutuhan sistem dan bisnis.
					</li><br>
					<li>Tahap analisis risiko<br>
					Tahap analisis reisiki berfungsi untuk mengidentifikasi resiko yang berpotensi akan terjadi dan menghasilkan solusi alternatif secara teknis dan manajemen saat strategi mitigasi (upaya untuk mengurangi resiko bencana) direncanakan dan diselesaikan.
					</li><br>
					<li>Tahapan rekayasa (engineering) <br>
					Pada tahap rekayasa, beberapa kegiatan ini yang akan dilakukan, yaitu:<br>
					1. Menguji, coding dan mengembangkan software <br>
					2. Menginstal software<br>
					3. Membuat prototype<br>
					4. Mendesain dokumen<br>
					5. Meringkas suatu pengujian software<br>
					6. Membuat laporan atas kekurangan dari software agar segera diperbaiki<br>
					<br>
					<li>Tahap Konstruksi dan Pelepasan (release):pada tahap ini dilakukan pembangunan perangkat lunak yang dimaksud, diuji, diinstal dan diberikan sokongan-sokongan tambahan untuk keberhasilan proyek.
					</li><br>
					<li>Tahap evaluasi<br>
					Pada tahap evaluasi, system analyst membutuhkan masukan dan tanggapan dari para user dalam mengevaluasi perangkat/produk yang diuji dan memastikan bahwa produk dibutuhkan sesuai ketentuan yang telah dibicarakan diawal dengan user. System analyst memastikan pelanggan puas dengan produk yang akan dihasilkan untuk menjawab persoalan bisnis mereka. Selain itu, system analyst harus tetap memantau resiko yang akan terjadi seperti faktor-faktor yang dapat menyebabkan cost overrun (pembengkakan biaya).
					</li> </ul>							   
								</p>
				<h3 class="text-dark mt-0">Kelebihan Metodologi</h3>
				   <p class="text-white mb-4" >
                   <ul style="color:white">
